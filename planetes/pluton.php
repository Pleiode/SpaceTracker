<h1>Pluton</h1>

<h4 style="margin-top: 30px;">Description</h4>
<p style="margin-top: 5px;" >Après sa découverte par l'astronome américain Clyde Tombaugh en 1930, Pluton était considérée comme 
la neuvième planète du Système solaire. À la fin du XXe siècle et au début du XXIe siècle, de plus en plus d'objets similaires furent 
découverts dans le Système solaire externe, en particulier Éris, alors estimé légèrement plus grand et plus massif que Pluton. 
Cette évolution amena l'Union astronomique internationale (UAI) à redéfinir la notion de planète, Cérès, Pluton et Éris étant depuis 
le 24 août 2006 classées comme des planètes naines. 
</p>

