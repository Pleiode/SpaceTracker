  <h1>Perseverance</h1>

  <h2 style="margin-top: 20px;">Mission</h2>
  <p>Pour remplir les objectifs de sa mission, Perseverance doit prélever une quarantaine de carottes de sol et de roches sur des sites sélectionnés
    à l'aide des instruments embarqués. Le résultat de ces prélèvements doit être déposé par l'astromobile sur des emplacements soigneusement repérés
    en attendant d'être ramenés sur Terre par une future mission étudiée conjointement par la NASA et l'Agence spatiale européenne.
  </p>

  <h2 style="margin-top: 40px;">Partenaire</h2>
  <p>

    SuperCam, une super caméra installée à la tête du rover, est un peu le « couteau suisse » des scientifiques de la mission. Le CNES, le CNRS et plusieurs universités et établissements
    français ont contribué à la construction de cet instrument. Il utilise cinq techniques d’analyse différentes : une mesure de composition atomique,
    deux mesures moléculaires (la façon dont les atomes sont liés entre eux et l’arrangement des molécules entre elles), un imageur pour photographier les cibles qui
    sont analysées et enfin le tout premier microphone scientifique à atteindre la surface de Mars. Ainsi équipé, SuperCam étudiera à distance la chimie et la minéralogie
    de Mars ou la composition de son atmosphère. </p>

  <h2 style="margin-top: 40px;">Date de lancement</h2>
