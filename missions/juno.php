     <h1>Juno</h1>

     <h2 style="margin-top: 20px;">Mission</h2>
     <p>La sonde Juno étudie la planète Jupiter au plus près. Elle étudie la composition de son atmosphère, sa structure interne, son champ magnétique,
          et l’origine de sa formation.
     </p>

     <h2 style="margin-top: 40px;">Partenaire</h2>
     <p>Dans l’importante participation européenne à cette mission américaine, le CNES a contribué à
          la réalisation par l’IRAP du senseur d’électrons JADE-E de l’instrument JADE. JADE détectera les particules énergétiques
          (électrons et ions) produites par les aurores polaires de Jupiter, complétant ainsi les autres instruments de Juno pour étudier sa magnétosphère.</p>

     <h2 style="margin-top: 40px;">Date de lancement</h2>
     <p>5 Août 2011</p>



