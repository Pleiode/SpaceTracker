  <h1>Calipso</h1>

  <h2 style="margin-top: 20px;">Mission</h2>
  <p>Orbitant à 705 km au-dessus de nos têtes, le mini-satellite CALIPSO (Cloud Aerosol
    Lidar and Infrared Pathfinder Satellite Observations) analyse les nuages et les aérosols depuis 2006.
    Des mesures destinées à mieux comprendre le rôle des nuages et des aérosols sur le climat.
  </p>

  <h2 style="margin-top: 40px;">Partenaire</h2>
  <p>Coopération entre le CNES et la NASA : le CNES a la responsabilité du satellite, met à disposition la plateforme
    et l’imageur infrarouge. Tandis que la NASA est responsable de la mission, fournit la charge utile et le lanceur.</p>

  <h2 style="margin-top: 40px;">Date de lancement</h2>
  <p>19 décembre 2013</p>



